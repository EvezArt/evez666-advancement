def parse_evez_os_corpus() -> Dict:
    """Extract knowledge from EVEZ-OS codebase"""
    corpus = {
        "concepts": [],
        "architectures": [],
        "fire_events": [],
        "patterns": []
    }
    
    # Parse key files
    key_files = [
        "evez_core.py",
        "attractor_lock.py", 
        "fire_approach.py",
        "emergence_saturation.py",
        "constitution/CONSTITUTION.md"
    ]
    
    for f in key_files:
        path = EVEZ_OS / f
        if path.exists():
            try:
                content = path.read_text()
                # Extract key concepts
                for word in re.findall(r'[A-Z][a-z]+[A-Z]\w+', content):
                    if word not in corpus["concepts"]:
                        corpus["concepts"].append(word)
                corpus["architectures"].append(f)
            except:
                pass
    
    return corpus

